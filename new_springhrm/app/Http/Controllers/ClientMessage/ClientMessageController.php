    {
        $request->validate([
            'client_sub' => 'required',
            'client_message' => 'required'
        ]);

        $message = new ClientMessage();

        $message->subject = $request->client_sub;
        $message->message = $request->client_message;

        $message->save();
        return redirect()->route('message.index');
    }

